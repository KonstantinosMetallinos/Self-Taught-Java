
class Ep25_Math_CLass_methods {
	public static void main(String args[]){
		System.out.println(Math.abs(-26.7));	// absolute value
		System.out.println(Math.ceil(7.4));		// ceiling (round up)
		System.out.println(Math.floor(7.8));	// floor (round down)
		System.out.println(Math.max(8.6,5.2));	// maximum(a,b)
		System.out.println(Math.min(8.6,5.2));	// minimum(a,b)
		System.out.println(Math.pow(5,3));		// Powers(a,b) -> a^b
		System.out.println(Math.sqrt(9));		// Square root(a)		
	}
}
